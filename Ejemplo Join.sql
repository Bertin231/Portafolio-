INNER JOIN PADRE P
ON H.PADRE_ID = P.ID_PADRE;

--USO DEL LEFT JOIN -- DEVULEVE TODOS LOS REGISTROS DE LA TABLA IZQUIERDA (HIJO) Y LOS REGISTROS COINCIDENTES DE LA TABLA DERECHA (PADRE)
SELECT * FROM HIJO H
LEFT JOIN PADRE P 
ON H.PADRE_ID = P.ID_PADRE;

--USO DEL LEFT JOIN QUITANDO COINCIDENCIAS 
SELECT * FROM HIJO H 
LEFT JOIN PADRE P 
ON H.PADRE_ID = P.ID_PADRE
WHERE H.PADRE_ID IS NULL;

--USO DEL RIGHT JOIN -- DEVUELVE TODOS LOS REGISTROS DE LA TABLA DERECHA QUE ES LA TABLA (PADRE)Y LOS REGISTROS COINCIDENTES CON LA TABLA IZQUIERDA QUE ES LA (HIJO)
SELECT * FROM HIJO H
RIGHT JOIN PADRE P 
ON H.PADRE_ID = P.ID_PADRE;

--USO DEL RIGHT JOIN SIN CONINCIDENCIAS 
SELECT * FROM HIJO H 
RIGHT JOIN PADRE P 
ON H.PADRE_ID = P.ID_PADRE
WHERE H.PADRE_ID IS NULL;

--USO DEL FULL JOIN -- DEVUELVE TODOS LOS REGISTROS DE AMBAS TABLAS Y SUS COINCIDENCIAS 

