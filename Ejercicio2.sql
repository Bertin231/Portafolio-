--Creacion de una entidad con DDL
/*
CHAR : ALAMACENA DATOS DE LONGITUD
CHARACTER: 
VARCHAR: 
VARCHAR2: SOLO ORACLE
NVARCHAR2: ESPECIFICO DE ORACLE, SE UTILIZA PARA ALAMACENAR CARACTERES DE LONGITUD VARIABLE DE CARACTER UNICIO
*/

--DDL
CREATE TABLE EMPLEADO(
  ID_EMPELADO NUMBER,
  NOMBRE NVARCHAR2(100),
  APELLIDO_P NVARCHAR2(100),
  EDAD NUMBER,
  PUESTO NVARCHAR2(100),
  DEPARTAMENTO NVARCHAR2(100)
);

--DML
INSERT INTO EMPLEADO VALUES(1,'JULIAN','RPFRIGUEZ',37,'ENCARGADO DE NOMINAS','RH');
